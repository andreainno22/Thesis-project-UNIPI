"""
Prepare the YOLO single-class ("obstruction") dataset for P1, from the DB.
====================================================================

Builds grouped K-fold cross-validation splits over the copy-paste dataset,
WITHOUT copying any image: it writes per-fold image-path list files plus one
Ultralytics `data.yaml` per fold. Ultralytics resolves each label by swapping
`/images/` -> `/labels/` in the path, so:

  * positives  -> Dataset/ostruzioni_reali/images/X.jpg
                  -> Dataset/ostruzioni_reali/labels/X.txt   (exists)
  * negatives  -> Dataset/non_ostruite/<venue>/Y.jpg
                  -> Dataset/non_ostruite/<venue>/Y.txt      (absent = background)

Data sources (all read from occlusion.db):
  * POSITIVES : occlusion_type = 'synthetic_copypaste'   (990 copy-paste imgs)
  * NEGATIVES : the held-out reference backgrounds NOT used by the copy-paste,
                listed in Dataset/ostruzioni_reali/pruned_backgrounds.txt
                (~10 %, seed 42). Using the used-backgrounds as negatives would
                leak, since the copy-paste positives are built on top of them.

Cross-validation grouping uses `source_group` (the background id) so the same
background can never appear in both train and val of a fold. Folds are
stratified by venue x (pos/neg) for balance.

The `poli ingegneria` real images (split='test' in the DB) are the held-out
test set and are intentionally NOT touched here.

Usage:
  python pipeline1/src/prepare_yolo_dataset.py \
      --db Aggregated_dataset_db/occlusion.db \
      --dataset-root Dataset \
      --out-dir pipeline1/data \
      --folds 5 --seed 42
"""

from __future__ import annotations

import argparse
import sqlite3
from pathlib import Path

try:
    from sklearn.model_selection import StratifiedGroupKFold
except ImportError as e:  # pragma: no cover
    raise SystemExit("scikit-learn is required: pip install scikit-learn") from e


# --------------------------------------------------------------------------- #
# DB access
# --------------------------------------------------------------------------- #
def fetch_positives(conn: sqlite3.Connection) -> list[dict]:
    rows = conn.execute(
        "SELECT frame_id, file_path, venue_type, source_group "
        "FROM frames WHERE occlusion_type = 'synthetic_copypaste'"
    ).fetchall()
    return [dict(r) for r in rows]


def fetch_negatives(conn: sqlite3.Connection, pruned_stems: set[str]) -> list[dict]:
    """Return the held-out normal reference frames used as YOLO backgrounds.

    Selected by matching `source_group` against the pruned-background stems.
    We restrict to the clean Pinterest normals (not shadow/light variants).
    """
    placeholders = ",".join("?" * len(pruned_stems))
    rows = conn.execute(
        f"SELECT frame_id, file_path, venue_type, source_group "
        f"FROM frames "
        f"WHERE is_normal = 1 AND source = 'Pinterest' "
        f"AND source_group IN ({placeholders})",
        tuple(pruned_stems),
    ).fetchall()
    return [dict(r) for r in rows]


# --------------------------------------------------------------------------- #
# pruned_backgrounds.txt parsing
# --------------------------------------------------------------------------- #
def parse_pruned_backgrounds(path: Path) -> set[str]:
    """Parse the indented stem list produced by the copy-paste generator."""
    stems: set[str] = set()
    for line in path.read_text(encoding="utf-8").splitlines():
        s = line.strip()
        if not s or "=" in s:  # skip 'ratio=0.1', 'seed=42', 'corridoi=55'
            continue
        stems.add(s)
    return stems


# --------------------------------------------------------------------------- #
# Fold construction
# --------------------------------------------------------------------------- #
def build_folds(samples: list[dict], n_folds: int, seed: int) -> list[int]:
    """Assign each sample a fold id via StratifiedGroupKFold.

    - group  = source_group (no background spans train/val)
    - strata = venue + label, to balance corridoio/porta and pos/neg per fold
    """
    groups = [s["source_group"] for s in samples]
    strata = [f"{s['venue_type']}_{s['label']}" for s in samples]
    X = list(range(len(samples)))

    fold_of = [0] * len(samples)
    sgkf = StratifiedGroupKFold(n_splits=n_folds, shuffle=True, random_state=seed)
    for k, (_, val_idx) in enumerate(sgkf.split(X, strata, groups)):
        for i in val_idx:
            fold_of[i] = k
    return fold_of


def write_list(path: Path, samples: list[dict], dataset_root: Path) -> None:
    lines = [str((dataset_root / s["file_path"]).resolve()) for s in samples]
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_yaml(path: Path, train_txt: Path, val_txt: Path) -> None:
    path.write_text(
        "# Auto-generated by prepare_yolo_dataset.py - do not edit by hand.\n"
        "# Single-class obstruction detection (P1).\n"
        f"train: {train_txt.resolve().as_posix()}\n"
        f"val: {val_txt.resolve().as_posix()}\n"
        "nc: 1\n"
        "names: [obstruction]\n",
        encoding="utf-8",
    )


# --------------------------------------------------------------------------- #
# main
# --------------------------------------------------------------------------- #
def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--db", default="Aggregated_dataset_db/occlusion.db")
    ap.add_argument("--dataset-root", default="Dataset")
    ap.add_argument("--out-dir", default="pipeline1/data")
    ap.add_argument("--pruned-file",
                    default="Dataset/ostruzioni_reali/pruned_backgrounds.txt")
    ap.add_argument("--folds", type=int, default=5)
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--limit", type=int, default=0,
                    help="Cap total images (smoke tests only; 0 = all).")
    args = ap.parse_args()

    dataset_root = Path(args.dataset_root)
    out_dir = Path(args.out_dir)
    folds_dir = out_dir / "folds"
    folds_dir.mkdir(parents=True, exist_ok=True)

    conn = sqlite3.connect(args.db)
    conn.row_factory = sqlite3.Row

    # ---- positives ----
    pos = fetch_positives(conn)
    for s in pos:
        s["label"] = "pos"

    # ---- negatives (held-out pruned backgrounds) ----
    pruned_stems = parse_pruned_backgrounds(Path(args.pruned_file))
    neg = fetch_negatives(conn, pruned_stems)
    for s in neg:
        s["label"] = "neg"

    found_stems = {s["source_group"] for s in neg}
    missing = pruned_stems - found_stems
    if missing:
        print(f"[warn] {len(missing)} pruned stems not found as Pinterest "
              f"normals in the DB (e.g. {sorted(missing)[:3]}). "
              f"Check source_group naming.")

    # sanity: positives must NOT overlap the pruned backgrounds
    used_groups = {s["source_group"] for s in pos}
    leak = used_groups & pruned_stems
    if leak:
        print(f"[warn] {len(leak)} pruned stems ALSO appear as copy-paste "
              f"backgrounds - possible leakage (e.g. {sorted(leak)[:3]}).")

    print(f"positives (copy-paste): {len(pos)}")
    print(f"negatives (pruned bg) : {len(neg)}  "
          f"(pruned list had {len(pruned_stems)})")

    samples = pos + neg
    if args.limit:  # smoke: keep a balanced-ish head
        half = args.limit // 2
        samples = pos[:half] + neg[: args.limit - half]
        print(f"[limit] using {len(samples)} images for a smoke run")

    # ---- verify files exist on disk ----
    missing_files = [s["file_path"] for s in samples
                     if not (dataset_root / s["file_path"]).exists()]
    if missing_files:
        print(f"[warn] {len(missing_files)} image files missing on disk, "
              f"e.g. {missing_files[:2]}")

    # ---- folds ----
    fold_of = build_folds(samples, args.folds, args.seed)

    print("\nfold  train  val   (pos/neg in val)")
    for k in range(args.folds):
        train = [s for s, f in zip(samples, fold_of) if f != k]
        val = [s for s, f in zip(samples, fold_of) if f == k]
        write_list(folds_dir / f"fold{k}_train.txt", train, dataset_root)
        write_list(folds_dir / f"fold{k}_val.txt", val, dataset_root)
        write_yaml(out_dir / f"fold{k}.yaml",
                   folds_dir / f"fold{k}_train.txt",
                   folds_dir / f"fold{k}_val.txt")
        vp = sum(1 for s in val if s["label"] == "pos")
        vn = len(val) - vp
        print(f"  {k}   {len(train):5d} {len(val):4d}   ({vp}/{vn})")

    # ---- all-data yaml (final model): all as train, fold0 val as a monitor ----
    write_list(folds_dir / "all_train.txt", samples, dataset_root)
    write_yaml(out_dir / "all.yaml",
               folds_dir / "all_train.txt",
               folds_dir / "fold0_val.txt")

    print(f"\nWrote {args.folds} fold yamls + all.yaml to {out_dir}")
    conn.close()


if __name__ == "__main__":
    main()
