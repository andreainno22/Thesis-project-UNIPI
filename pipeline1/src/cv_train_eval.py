"""
10-fold cross-validation: train each model on every fold, score it on GEMINI.
====================================================================

Why evaluate on gemini and not on the CV validation fold: the CV fold is
copy-paste (synthetic), i.e. the SAME distribution as the training data, and
that metric is saturated (mAP50-95 ~0.97-0.99 for every model) and does NOT
predict real-world behaviour - rtdetr-l is the BEST on synthetic and the WORST
on gemini. Running statistical tests on it would rigorously answer the wrong
question ("who fits the synthetic data best?").

Design used here instead: the folds provide the variance due to training-set
resampling (10 differently-trained models per architecture), and each of those
models is scored on the SAME external test set (gemini). That yields 10 paired
measurements per model on the metric we actually care about, which is exactly
what a paired t-test / Wilcoxon signed-rank needs.

Writes one row per (model, fold) to --out-csv; feed it to cv_stats.py.

Usage (on the GPU VM, after gemini has been synced there):
  python3 pipeline1/src/prepare_yolo_dataset.py \
      --db Aggregated_dataset_db/occlusion.db --dataset-root Dataset \
      --out-dir pipeline1/data_noleak_10 --folds 10 --seed 42 \
      --exclude-stems-file pipeline1/data/gemini_bg_exclude.txt

  python3 -u pipeline1/src/cv_train_eval.py \
      --folds-dir pipeline1/data_noleak_10 \
      --models yolo11n yolo11s yolo11m yolo26n yolo26s yolo26m \
      --epochs 150 --patience 20 --batch 32 --device 0 \
      --out-csv pipeline1/results/cv10_gemini.csv
"""

from __future__ import annotations

import argparse
import csv
import time
from pathlib import Path

import pandas as pd
from ultralytics import YOLO


def write_gemini_yaml(dataset_root: Path, out: Path) -> Path:
    """Build a platform-correct gemini val yaml (paths resolved at runtime)."""
    root = (dataset_root / "ostruzioni_gemini").resolve()
    for venue in ("corridoi", "porte"):
        if not (root / venue / "images").is_dir():
            raise SystemExit(f"gemini images not found: {root / venue / 'images'}")
    out.write_text(
        "# auto-generated by cv_train_eval.py - do not edit\n"
        f"path: {root.as_posix()}\n"
        "train: corridoi/images  # unused placeholder, val-only\n"
        "val:\n"
        "  - corridoi/images\n"
        "  - porte/images\n"
        "nc: 1\n"
        "names: [obstruction]\n",
        encoding="utf-8",
    )
    return out


def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--folds-dir", default="pipeline1/data_noleak_10")
    ap.add_argument("--dataset-root", default="Dataset")
    ap.add_argument("--models", nargs="+",
                    default=["yolo11n", "yolo11s", "yolo11m",
                             "yolo26n", "yolo26s", "yolo26m"],
                    help="model stems; '.pt' is appended. RT-DETR is excluded "
                         "on purpose (40%% of the GPU budget, not a deploy "
                         "candidate); if you add it, pass --no-amp-models rtdetr-l")
    ap.add_argument("--no-amp-models", nargs="*", default=["rtdetr-l"],
                    help="models needing amp=False (RT-DETR NaNs with AMP)")
    ap.add_argument("--epochs", type=int, default=150)
    ap.add_argument("--patience", type=int, default=20)
    ap.add_argument("--batch", type=int, default=32)
    ap.add_argument("--imgsz", type=int, default=640)
    ap.add_argument("--device", default="0")
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--project", default="pipeline1/runs_cv10")
    ap.add_argument("--out-csv", default="pipeline1/results/cv10_gemini.csv")
    args = ap.parse_args()

    folds_dir = Path(args.folds_dir)
    fold_yamls = sorted(folds_dir.glob("fold*.yaml"),
                        key=lambda p: int(p.stem.replace("fold", "")))
    if not fold_yamls:
        raise SystemExit(f"no fold*.yaml in {folds_dir}")

    gem_yaml = write_gemini_yaml(Path(args.dataset_root),
                                 folds_dir / "_gemini_val.yaml")
    print(f"{len(fold_yamls)} folds x {len(args.models)} models "
          f"= {len(fold_yamls) * len(args.models)} runs")
    print(f"gemini val yaml: {gem_yaml}")

    out_csv = Path(args.out_csv)
    out_csv.parent.mkdir(parents=True, exist_ok=True)
    # resume support: skip (model, fold) pairs already recorded
    done: set[tuple[str, int]] = set()
    if out_csv.exists():
        prev = pd.read_csv(out_csv)
        done = {(r.model, int(r.fold)) for r in prev.itertuples()}
        print(f"resuming: {len(done)} runs already in {out_csv}")

    fields = ["model", "fold", "params_M", "train_s",
              "synth_map50", "synth_map", "gem_map50", "gem_map", "gem_p", "gem_r"]
    new_file = not out_csv.exists()
    with out_csv.open("a", newline="", encoding="utf-8") as fh:
        w = csv.DictWriter(fh, fieldnames=fields)
        if new_file:
            w.writeheader()

        for stem in args.models:
            amp = stem not in args.no_amp_models
            for y in fold_yamls:
                k = int(y.stem.replace("fold", ""))
                if (stem, k) in done:
                    print(f"[skip] {stem} fold{k} (already done)")
                    continue
                name = f"{stem}_f{k}"
                print(f"\n=== {stem} fold{k}  (amp={amp}) ===", flush=True)
                t0 = time.time()
                model = YOLO(f"{stem}.pt")
                model.train(
                    data=str(y.resolve()), epochs=args.epochs,
                    imgsz=args.imgsz, batch=args.batch, patience=args.patience,
                    amp=amp, device=args.device, seed=args.seed,
                    project=str(Path(args.project).resolve()), name=name,
                    exist_ok=True, single_cls=True, verbose=False, plots=False,
                )
                train_s = time.time() - t0

                best = Path(args.project) / name / "weights" / "best.pt"
                scored = YOLO(str(best))
                n_par = sum(p.numel() for p in scored.model.parameters())

                # synthetic (same-distribution) score, from this fold's own val
                res = pd.read_csv(Path(args.project) / name / "results.csv")
                srow = res.loc[res["metrics/mAP50-95(B)"].idxmax()]

                # external test set: the metric that matters
                g = scored.val(data=str(gem_yaml.resolve()), device=args.device,
                               verbose=False, plots=False,
                               project=str(Path(args.project).resolve()),
                               name=f"{name}_gem", exist_ok=True)

                row = {
                    "model": stem, "fold": k,
                    "params_M": round(n_par / 1e6, 2),
                    "train_s": round(train_s, 1),
                    "synth_map50": round(float(srow["metrics/mAP50(B)"]), 5),
                    "synth_map": round(float(srow["metrics/mAP50-95(B)"]), 5),
                    "gem_map50": round(float(g.box.map50), 5),
                    "gem_map": round(float(g.box.map), 5),
                    "gem_p": round(float(g.box.mp), 5),
                    "gem_r": round(float(g.box.mr), 5),
                }
                w.writerow(row)
                fh.flush()   # survive a crash / preemption
                print(f"[done] {stem} fold{k}: gem mAP50={row['gem_map50']:.4f} "
                      f"mAP50-95={row['gem_map']:.4f}  ({train_s/60:.1f} min)",
                      flush=True)

    print(f"\nAll runs recorded in {out_csv}")
    print("Next: python pipeline1/src/cv_stats.py --csv " + str(out_csv))


if __name__ == "__main__":
    main()
